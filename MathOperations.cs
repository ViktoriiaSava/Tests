
namespace Pr2Tests
{
    public class MathOperations
    {
        public int A(int a, int b)
        {
            return a + b;
        }
        public int S(int a, int b)
        {
            return a - b;
        }
        public int M(int a, int b)
        {
            return a * b;
        }
        public int D(int a, int b)
        {
            return a / b;
        }

    }
